//------------------------------------------------------------------------------
// toy-ub.c - A strict aliasing UB
//------------------------------------------------------------------------------
#include <stdio.h>

int f(int *p, float *q)
{
    *p = 1;
    *q = 2.;
    return *p;
}

int main()
{
    int i;
    printf("%d\n", f(&i, (float*)(&i)));
    return 0;
}
